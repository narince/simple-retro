export function useTranslation() {
    const language = useAppStore((state) => state.language);

    const t = (key: keyof typeof translations['en'], options?: Record<string, string | number>) => {
        let text = translations[language][key] || key;
        if (options) {
            Object.keys(options).forEach(opt => {
                text = text.replace(`{{${opt}}}`, String(options[opt]));
            });
        }
        return text;
    };

    return { t, language };
}
